import { MarkerClusterer } from "@googlemaps/markerclusterer";


let map: google.maps.Map;
const middleOfCzechia: google.maps.LatLngLiteral = { lat: 49.74378, lng: 15.33865 };


export function initMap(): void {

    const mapOptions = {
        center: middleOfCzechia,
        mapId: "c2aa51b8ca67932f",
        zoom: 8,
        minZoom: 8,
        maxZoom: 17,
        dragabble: false,
        disableDefaultUI: true,
    }

    const markerIcon = {
        url: 'wwwroot/images/icons/marker.png',
    }

    map = new google.maps.Map(document.getElementById('map') as HTMLElement, mapOptions)

    const infoWindow = new google.maps.InfoWindow({
        content: '',
        disableAutoPan: true,
    })

    const markers = references.map((position) => {
        const label = position.name.charAt(0);
        const marker = new google.maps.Marker({
            position,
            label,
            icon: markerIcon,
            title: position.name
        })

        marker.addListener('click', () => {
            infoWindow.setContent(
                `<div class='map__pop-up'><strong>${position.name}</strong><br><span>${position.date}</span><p>${position.desc}</p><b>${position.price} Kč</b></div>`,
            )
            infoWindow.open(map, marker)
        })

        return marker
    })

    new MarkerClusterer({
        map,
        markers,
    })
}


